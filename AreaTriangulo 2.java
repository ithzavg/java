public class AreaTriangulo {
    public static void main(String[] args) {
        int base = 10;
        int altura = 3;
        
        double baseDecimal = 10.4;
        double alturaDecimal = 3.3;
        
        double resultadoDecimal = baseDecimal * alturaDecimal / 2;
        
        int resultado = base * altura / 2;
        
        System.out.println(resultado);
        System.out.println(resultadoDecimal);
    }
}
